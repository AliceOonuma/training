package jp.genuine.training.carmanager.model.user;

public class User {

	private String value;

	public User() {
		value ="";
	}

	public User(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}



}
